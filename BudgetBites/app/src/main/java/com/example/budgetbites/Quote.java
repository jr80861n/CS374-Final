package com.example.budgetbites;

public class Quote {
    private int id;
    private String advice;


    public Quote() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getQuote() {
        return advice;
    }

    public void setAdvice(String advice) {
        this.advice = advice;
    }


}
